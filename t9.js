// Arithmatic Operators-->  +, -, /, *, % (Modulas), ** (Exponent)for power, ++(increment), --(decrement)
// Assingnment Operator-->  =, +=, -=, *=, /=, %=, **=

var x = 5;
x += 6;  // that means x = x+6
x -= 6;  //that means x = x-6
x *= 6;   //that means x = x*6
x /= 6;   //that means x = x/6
x %= 6;   // that means x = x%6

console.log(x);
